def find_common_participants(group1, group2, delimiter=','):
    # Разбиваем строку group1 на список участников, используя указанный разделитель
    # Например: "Иван,Петр,Мария".split -> ['Иван', 'Петр', 'Мария']
    list1 = group1.split(delimiter)

    # Разбиваем строку group2 на список участников
    list2 = group2.split(delimiter)

    # Создаем пустой список для хранения общих участников
    common = []

    # Перебираем каждого участника из первой группы
    for participant in list1:
        # Проверяем два условия:
        # 1. Участник есть во второй группе
        # 2. Участник еще не добавлен в список общих
        # Это нужно, чтобы избежать дубликатов, если в первой группе есть повторяющиеся имена
        if participant in list2 and participant not in common:
            # Если оба условия выполняются, добавляем участника в список общих
            common.append(participant)

    # Возвращаем отсортированный в алфавитном порядке список общих участников
    return sorted(common)