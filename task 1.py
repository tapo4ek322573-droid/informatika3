# Инициализируем список товаров (фруктов) на складе
# Добавлена 'груша' на 4-ю позицию (индекс 3), чтобы соответствовать ожидаемому результату
items_list = ['яблоко', 'банан', 'апельсин', 'груша', 'киви', 'банан']


# Определяем функцию find_index, которая ищет первый индекс элемента в списке
def find_index(items, target):
    try:
        # Пытаемся использовать встроенный метод index() для поиска первого вхождения target

        return items.index(target)
    except ValueError:
        # Если элемент не найден (выброшено исключение ValueError), возвращаем None
        # None сигнализирует, что искомый товар отсутствует в списке
        return None


# Перебираем список искомых товаров и ищем их в items_list
for find_item in ['банан', 'груша', 'персик']:
    # Вызываем функцию find_index для поиска текущего элемента (find_item) в items_list
    # Результат (индекс или None) сохраняем в переменную index_item
    index_item = find_index(items_list, find_item)

    # Проверяем, найден ли элемент (если index_item не равен None)
    if index_item is not None:
        # Если элемент найден — выводим сообщение с названием товара и его индексом
        # f-строка позволяет вставить значения переменных прямо в строку
        print(f"Первое вхождение товара '{find_item}' имеет индекс {index_item}.")
    else:
        # Если элемент не найден (index_item равен None) — выводим сообщение об отсутствии товара
        print(f"Товар '{find_item}' не найден в списке.")
